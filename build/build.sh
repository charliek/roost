#!/usr/bin/env bash
# Roost build orchestrator.
#
# Two stages:
#   1. libghostty — clone Ghostty at a pinned SHA into build/ghostty-src,
#      run `zig build lib-vt` to produce libghostty-vt + ghostty/vt.h, install
#      to build/out/.
#   2. build — cgo-link against build/out and produce the roost + roost-cli
#      binaries at the repo root.
#
# Usage:
#   ./build/build.sh libghostty   # stage 1 only
#   ./build/build.sh build        # stage 2 only (assumes stage 1 done)
#   ./build/build.sh              # both
set -euo pipefail

# --- Pinned versions ---------------------------------------------------------
# Ghostty SHA — bump deliberately, in its own PR, with a rebuild test.
# KEEP IN SYNC with third_party/ghostty/build.sh until the Phase 9 cutover
# described in docs/development/vision.md. Both scripts must pin the same
# SHA so the legacy Go binary and the new Rust+Swift toolchain build against
# identical libghostty-vt artifacts.
GHOSTTY_SHA="c74f6d56d1feef473033057bc0ff7e3f00cf6421"  # 2026-04-25, builds lib-vt cleanly with zig 0.15.1
GHOSTTY_REPO="https://github.com/ghostty-org/ghostty.git"

# --- Paths -------------------------------------------------------------------
ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
BUILD_DIR="${ROOT_DIR}/build"
GHOSTTY_SRC="${BUILD_DIR}/ghostty-src"
OUT_DIR="${BUILD_DIR}/out"

# On macOS 26+ on Apple Silicon, the system libSystem.tbd ships only
# arm64e-macos targets, but Zig 0.15.2 builds its host build runner as
# plain arm64-macos — every libc symbol comes back undefined at link
# time. Pinned Zig and Ghostty SHA can't be bumped lightly (the
# Ghostty build.zig requires Zig 0.15.2 exactly), so we redirect
# Zig's `xcrun --sdk macosx --show-sdk-path` lookup to a sibling SDK
# that still exposes arm64-macos. No-op on Linux and on macOS where
# the default SDK is already arm64-compatible.
maybe_arm64_sdk_shim() {
  [ "$(uname -s)" = "Darwin" ] || return 0
  [ "$(uname -m)" = "arm64" ]  || return 0
  command -v xcrun >/dev/null 2>&1 || return 0

  local default_sdk
  default_sdk="$(xcrun --sdk macosx --show-sdk-path 2>/dev/null || true)"
  [ -n "${default_sdk}" ] || return 0
  if sdk_has_plain_arm64 "${default_sdk}"; then
    return 0
  fi

  local replacement=""
  local candidate
  for candidate in \
    /Library/Developer/CommandLineTools/SDKs/MacOSX15.sdk \
    /Library/Developer/CommandLineTools/SDKs/MacOSX15.4.sdk \
    /Library/Developer/CommandLineTools/SDKs/MacOSX14.sdk \
    /Applications/Xcode.app/Contents/Developer/Platforms/MacOSX.platform/Developer/SDKs/MacOSX15.sdk
  do
    if [ -d "${candidate}" ] && sdk_has_plain_arm64 "${candidate}"; then
      replacement="${candidate}"
      break
    fi
  done

  if [ -z "${replacement}" ]; then
    echo "error: Zig 0.15.2 needs an arm64-macos libSystem.tbd, but the system SDK"
    echo "       at ${default_sdk} only exposes arm64e on this machine,"
    echo "       and no sibling MacOSX1[45].sdk with plain arm64 was found."
    echo "       Install Xcode Command Line Tools (which usually ships an older"
    echo "       MacOSX15.sdk alongside the current one), or bump the pinned"
    echo "       Ghostty SHA + Zig version in build/build.sh."
    exit 1
  fi

  echo "==> macOS 26 SDK lacks arm64-macos in libSystem; redirecting xcrun to ${replacement}"
  local shim_dir
  shim_dir="$(mktemp -d -t roost-xcrun-shim)"
  cat > "${shim_dir}/xcrun" <<EOF
#!/usr/bin/env bash
# Generated by build/build.sh. Forces Zig's SDK lookup to return an
# arm64-compatible SDK on macOS 26+. All other xcrun calls fall through.
for arg in "\$@"; do
  case "\$arg" in
    --show-sdk-path) echo "${replacement}"; exit 0 ;;
  esac
done
exec /usr/bin/xcrun "\$@"
EOF
  chmod +x "${shim_dir}/xcrun"
  export PATH="${shim_dir}:${PATH}"
}

sdk_has_plain_arm64() {
  local tbd="$1/usr/lib/libSystem.tbd"
  [ -f "${tbd}" ] || return 1
  # libSystem.tbd is a multi-document YAML: the first document is the
  # libSystem entry itself; subsequent documents are re-exported
  # sub-libraries that may still list arm64-macos even when libSystem
  # itself is arm64e-only on macOS 26+. Restrict the match to the
  # first document.
  awk '/^---/ && ++n>1 { exit } { print }' "${tbd}" \
    | grep -Eq '(^|[[:space:],])arm64-macos([[:space:],]|$)'
}

stage_libghostty() {
  command -v zig >/dev/null 2>&1 || {
    echo "error: zig not found on PATH. Run \`mise install\` from the repo root."
    exit 1
  }

  if [ ! -d "${GHOSTTY_SRC}/.git" ]; then
    echo "==> Cloning Ghostty @ ${GHOSTTY_SHA}"
    git clone "${GHOSTTY_REPO}" "${GHOSTTY_SRC}"
  fi

  pushd "${GHOSTTY_SRC}" >/dev/null
  current_sha="$(git rev-parse HEAD)"
  if [ "${current_sha}" != "${GHOSTTY_SHA}" ]; then
    echo "==> Checking out ${GHOSTTY_SHA}"
    git fetch --quiet origin "${GHOSTTY_SHA}" || git fetch --quiet
    git checkout --quiet "${GHOSTTY_SHA}"
  fi

  echo "==> Building libghostty-vt"
  maybe_arm64_sdk_shim
  zig build -Demit-lib-vt=true -Doptimize=ReleaseFast --prefix "${OUT_DIR}"
  popd >/dev/null

  if [ ! -f "${OUT_DIR}/include/ghostty/vt.h" ]; then
    echo "error: ghostty/vt.h not found in ${OUT_DIR}/include after zig build."
    echo "       Inspect ${OUT_DIR} and ${GHOSTTY_SRC}/zig-out/."
    exit 1
  fi
  echo "==> libghostty-vt built: ${OUT_DIR}"
}

stage_build() {
  command -v go >/dev/null 2>&1 || {
    echo "error: go not found on PATH. Run \`mise install\` from the repo root."
    exit 1
  }

  if [ ! -f "${OUT_DIR}/include/ghostty/vt.h" ]; then
    echo "error: libghostty-vt not built yet. Run: $0 libghostty"
    exit 1
  fi

  export CGO_CFLAGS="-I${OUT_DIR}/include"
  # Don't set CGO_LDFLAGS="-L... -lghostty-vt" here. The cgo directive in
  # internal/ghostty/terminal.go already statically links libghostty-vt.a
  # by absolute path. Adding -lghostty-vt makes macOS ld prefer the dylib
  # over the static archive, embedding @rpath/libghostty-vt.dylib in the
  # binary; without a matching -Wl,-rpath the result aborts at launch with
  # `dyld: Library not loaded: @rpath/libghostty-vt.dylib / Reason: no
  # LC_RPATH's found`. Static-only is what we want.

  echo "==> Building roost"
  cd "${ROOT_DIR}"
  go build -o ./roost ./cmd/roost
  go build -o ./roost-cli ./cmd/roost-cli
  echo "==> Done: ./roost ./roost-cli"
}

stage="${1:-all}"
case "${stage}" in
  libghostty) stage_libghostty ;;
  build)      stage_build ;;
  all|"")     stage_libghostty; stage_build ;;
  *)          echo "usage: $0 [libghostty|build|all]"; exit 1 ;;
esac
